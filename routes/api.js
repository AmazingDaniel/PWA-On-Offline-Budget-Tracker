const router = require("express").Router();
const Deal = require("../models/transaction.js");

router.post("/api/deal", ({body}, res) => {
  Deal.create(body)
    .then(dbTransaction => {
      res.json(dbTransaction);
    })
    .catch(err => {
      res.status(404).json(err);
    });
});

router.post("/api/deal/bulk", ({body}, res) => {
  console.log(body)
  Deal.insertMany(body)
    .then(dbTransaction => {
      res.json(dbTransaction);
    })
    .catch(err => {
      res.status(404).json(err);
    });
});

router.get("/api/deal", (req, res) => {
  Deal.find({}).sort({date: -1})
    .then(dbTransaction => {
      res.json(dbTransaction);
    })
    .catch(err => {
      res.status(404).json(err);
    });
});

module.exports = router;